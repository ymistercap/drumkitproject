document.addEventListener('keydown', function (event) {
    if (event.keyCode === 81) {
        document.querySelector('#dclap').style.border = '.4rem solid yellow';
        document.querySelector('#clap').play();
        document.querySelector('#clap').currentTime = 0;
    }
});

document.addEventListener('keyup', function (event) {
    if (event.keyCode === 81) {
        document.querySelector('#dclap').style.border = '.4rem solid black';
    }
});

